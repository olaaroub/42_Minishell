#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*head;
	t_list	*new;
	void	*d;

	if (!lst || !f || !del)
		return (NULL);
	head = NULL;
	while (lst)
	{
		d = f(lst->content);
		new = ft_lstnew(d);
		if (!new)
		{
			del(d);
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, new);
		lst = lst->next;
	}
	return (head);
}
